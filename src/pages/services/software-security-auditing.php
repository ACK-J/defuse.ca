<h1>Is your software secure?</h1>

<p>
I want to work with you to make sure your code is secure. So I've decided to
start a part-time security auditing service. I'm available 10 hours per week, for
$20 CAD per hour, to look over your designs and code to make sure they are
secure. To give back to the community, I'll reduce my rate to $15 per hour for
published free software (under a GPL-like license).
</p>

<p>
I am not an experienced code auditor, but it's something I <a
href="http://www.cryptofails.com/">really like to do</a>,
and I am pretty good with security (see my <a
href="/resume.htm">r&eacute;sum&eacute;</a>). So this will also be a learning experience for
me. That's good for you, too: I will not charge for the time I need to spend
