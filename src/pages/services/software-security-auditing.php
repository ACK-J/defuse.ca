    <li>Look for memory corruption bugs in C and C++ code.</li>
    <li>Write proof-of-concept exploits for vulnerabilities.</li>
    <li>Check the security of your Linux server and the services it runs.</li>
</ul>

<p>
I am especially interested in auditing software that uses cryptography.
</p>

<p>
To make the most effective use of my limited time, I will work with you to
prioritize which parts of your application should be audited first. When the
audit is complete, I will work with you to ensure the problems are fixed
correctly.
</p>

