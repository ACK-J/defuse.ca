<h1>Rules For Working With Me</h1>

<p>
If I am paying you to do something, here are some rules that will help us get
along and be more efficient:
</p>

<ul>
    <li>
        <p>
        <b>I'm probably wrong.</b>
If I know how to do everything, and it's just a matter of time, I will usually
do as much as I can myself. That's how I work. So, if I'm paying you, it means
I think you're <em>better</em> than me at what I'm paying you for.  There's
a good chance you'll come up with better, easier, or faster ways of doing
things.  If so, tell me. Don't be afraid to argue your point.
        </p>
    </li>
    <li>
        <p>
<b>Don't worry about my time.</b> If I'm paying you for something, my number one
priority is to make sure you can make the most of the time I'm paying for. Don't
be afraid to ask stupid or obvious questions. Your emails will always be
prioritized. I prefer short emails (see <a href="http://emailcharter.org/">The
Email Charter</a>), but if it's a really complicated problem, I won't mind
reading a 10-page email. Frequent (but short) progress updates are appreciated.
        </p>
    </li>
    <li>
        <p>
<b>Be realistic about the cost.</b> You are a skilled human being. When I ask
for a quote, do not undervalue yourself and your skills. Feel free to quote
high. If it's too much, I'll tell you. Then you can make a <em>rational</em>
decision to lower the price. I'd rather you be happy with your pay, so that
you're happy to work on the project, than to be frustrated all the time because
you aren't being paid enough.
        </p>
    </li>
    <li>
        <p>
            <b>Don't be afraid to quit.</b>
If you lose interest in the project and can't continue working efficiently, let
me know as soon as possible. I won't be mad. I will always pay you for the work that
you've done.
        </p>
    </li>
    <li>
        <p>
            <b>Don't let work take over your life.</b>
I don't care what you do outside of work. As long as it doesn't significantly
impact your performance or harm the project. Your human rights apply here.
        </p>
    </li>
    <li>
        <p>
            <b>You know yourself best.</b>
You know how to get the most out of your time. You know that you would be twice
as efficient if you only had to work 6 hours a day, or had a 3 hour lunch break,
or whatever. Tell me, and I will try to accomodate it.
        </p>
    </li>
</ul>

<p>
If you are paying me to do something, invert the rules above.
</p>
