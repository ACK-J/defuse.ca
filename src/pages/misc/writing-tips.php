<h1>Writing Tips</h1>

<p>
I don't consider myself to be an excellent writer, but I do read a lot. As
I read, I notice certain things that throw me off. I'll have to re-read the
sentence slower, wasting a lot of time. This page lists those things so that
everyone can benefit by not using them in their writing.
</p>

<ul>
    <li>
    <p>
        Do not use &quot;the former&quot; or &quot;the latter.&quot; Give each
        item in the list a name, and refer to items by name. I don't have time
        to re-read the previous sentence to figure out which one came first or
        last.
    </p>
    </li>
    <li>
    <p>
        Avoid &quot;is it ... ?&quot; unless it is the begining of a sentence, or the
        question mark appears on the same line. I always confuse this with
        &quot;it is&quot; and read the sentence wrong.
    </p>
    </li>
</ul>
