<h1>Known Unknowns</h1>

<p>
This page lists things that I am aware of but do not understand. They are "known
unknowns."
</p>

<p>
It is not my goal to learn about everything on this list, for that would be
impossible. The list will necessarily expand as I learn more and more. I doubt
it is possible to learn one thing without acquiring at least two more known
unknowns. The purpose of the list is to remind me of the things I do not know,
and perhaps to guide me while learning about the universe in which I exist. I am
publishing it so others may use it to start their own list.
</p>

<p>
It is difficult to characterize what sorts of things are worth adding to the
list. I cannot explain why knowing about rocks on Mars is more important than
memorizing the arrangement of stones in the street in front of my house. I will
never directly experience the former, whereas I experience the latter almost
every day. I can only say that permutations of rocks are uninteresting to me
unless they are something like 225,000,000 km away. For most people, the
permutations of fabric that cover some individuals at events like The Academy
Awards is somewhat interesting. To me, it is much less interesting than the
arrangement of stones outside my house.
</p>

