<h1>Honestyware&trade;</h1>

<h2>Honestyware in a Nutshell</h2>

<p>Honestyware is a philosophy for selling software (and other digital content) that lets the consumer decide whether the software is worth paying for, and how much the software is worth. It is based on the assumption&mdash;and my strong belief&mdash;that <strong>people will pay for things that are worth paying for</strong>.</p>

<p>Software sold as honestyware can be downloaded free of charge, but if the user likes the software they must pay for it. If the user doesn't like the software, they must delete it from their system as a courtesy to the software developers. These rules are <strong>not enforced</strong>, so it's up to the user to be honest and obey them&mdash;that's why it's called honestyware. Because the rules are not enforced, software sold as honestyware is never encumbered by bloated DRM technologies or trial limitations. I truly believe that people will follow the rules and pay for good software even if they can get it for free.</p>

<p>Sharing honestyware is legal, and even recommended. The money honestyware loses to dishonest users is quickly reclaimed by social sharing. People are more likely to share content that is accessible for free than content behind a pay wall. Besides, nearly all proprietary software is available for free on software piracy websites. If a dishonest user wants to get some piece of software for free, they will. Honestyware cuts out the middle step and embraces piracy as a form of advertising.</p>

<h2>Formal Definition of Honestyware</h2>

<p>Honestyware is any computer software that meets <strong>all</strong> of the following criteria:</p>

<ol>
    <li>The full, unlimited, and official version of the software can be downloaded without charge or effort. For example, making the user register an account on a website to download the software is not acceptable, but displaying extra ads on the download page is OK.</li>
    <li>Users must not be charged any type of fee (e.g. monthly subscription) to use the software.</li>
    <li>The user may distribute copies of the software without charge or penalty; even if the user has not paid for the software.</li>
    <li>The software may present an <strong>unobtrusive</strong> message to the user informing them that the software is honestyware and must be paid for only if it does not interfere with the software's functionality in any way.</li>
    <li>Users must be able to purchase the software for any price they feel the software is worth. The software vendor must not set a minimum nor a maximum price, but can list a suggested price.</li>
    <li>The software does not use DRM technologies to limit its use, enforce "Activation" or "Registration" requirements, or attempt to "lock" itself to a computer to prevent redistribution.</li>
</ol>

<p>Any software that does not meet all of the above criteria is not honestyware and cannot be marketed as such.</p>

<h2>Is Honestyware a feasible way to sell software?</h2>

<p>To be honest, I don't know yet. I'm going to sell some software as honestyware to find out. I have very good reasons to believe honestyware is a feasible way to sell software, though:</p>
<ul>
    <li>Digital content can be reproduced for free, so money is never strictly lost by allowing users to download software for free.</li>
    <li>Because users can choose their own price, those who cannot pay the full price for the software can just pay less instead of pirating the software and not paying a cent to the developer.</li>
    <li>Honestyware can be shared, so it will reach a greater audience through unsolicited social advertising.</li>

</ul>

<h2>Why is Honestyware Better for the User?</h2>

<p>"Better" is a broad term, but here are a few arguments why honestyware is better for the user:</p>

<ul>
    <li>Honestyware is not encumbered by DRM.</li>
    <li>Users will only pay for software they like, so honestyware encourages the development of high quality software.</li>
    <li>Users can share honestyware without feeling like they are obligating friends or family members to purchase the software.</li>
    <li>Users can try the full version of the software to see if they like it before paying for it.</li>
    <li>Open Source and Free (as in speech) software can be sold as honestyware.</li>
</ul>

