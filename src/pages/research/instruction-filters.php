<h1>Instruction Set Filters</h1>

<p>
In the summer of 2013, I worked with <a
href="http://pages.cpsc.ucalgary.ca/~locasto/">Dr. Michael E. Locasto</a> on
a research project to determine whether disabling CPU instructions based on the
current execution context could serve as an effective exploit defense mechanism.
The research is not complete (I'm trying to find time to finish it!), but you
can find the slides from a presentation I gave <a
href="https://defuse.ca/files2/insfilter.pdf">here</a>. The <a
href="/bochs-hacking-guide.htm">Bochs Hacking Guide</a> is also a result of this
research.
</p>
