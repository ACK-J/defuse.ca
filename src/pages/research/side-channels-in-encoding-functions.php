<h1>Open Problem: Side Channel Attacks on Encoding Functions</h1>

<p>
Encoding functions like <kbd>bin2hex()</kbd> and <kbd>base64_encode()</kbd> are
very common in web applications. PHP does not implement these functions in
constant time. Question: Can the timing/cache differences be used to compromise
privacy in web applications? Also, how can one safely encode a binary string to
base64 or hex in PHP?
</p>

