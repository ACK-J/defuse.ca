<h1>Online Password Policy Statistics</h1>

<p>Many online account-based services enforce a rather small limit on password length. Some even restrict what kinds of characters can be used in passwords. These kinds of restrictions actually decrease password security, and as I will explain later, are a good sign that the website is storing everyone's password in plain text instead of properly <a href="http://crackstation.net/hashing-security.html">hashing them with salt</a>.</p>

<h2>Statistics</h2>
<p>To measure how widespread unreasonable password restrictions are, I used Alexa's <a href="http://www.alexa.com/topsites">Top Sites</a> list to manually gather statistics. I recorded the password policies of two gropus of websites - the top 100 sites and a 100-site random sample of the top 1,000,000 sites.</p>
<p>During my research, I realized that non-english websites, especially Chinese, were adding a huge bias to my results. Most Asian websites restrict passwords to very few characters. I don't understand enough about these foreign languages to make authoratative claims, so I have simply excluded non-english websites from the results.</p>

